Umur=int(input("Masukkan Umur anda : "))
if Umur > 50:
    print ("Anda Tua")
elif Umur > 25:
    print ("Anda Dewasa")
elif Umur > 17:
    print ("Anda Remaja")
elif Umur > 7:
    print ("Anda Anak-anak")
elif Umur > 0:
    print ("Anda masih dibawah Umur")
else :
    print ("Terimakasih sudah mengisi, umur anda tidak wajar untuk di logika")
